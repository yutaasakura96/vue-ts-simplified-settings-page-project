<template>
  <main class="max-w-2xl mx-auto px-4">
    <nav class="font-medium text-center text-gray-500 border-b border-gray-200 mb-4">
      <ul class="flex flex-wrap -mb-px">
        <li v-for="tab in tabs" :key="tab.key">
          <TabLink :tab="tab" :currentTab="currentTab" @click="currentTab = tab.key" />
        </li>
      </ul>
    </nav>
    <Transition mode="out-in" name="fade"><component :is="currentTabComponent" /></Transition>
  </main>
</template>

<script setup lang="ts">
import { ref, computed } from 'vue';
import type { Tab, TabKey } from './types';
import TabLink from './components/TabLink.vue';
import GeneralSettings from './components/GeneralSettings.vue';
import NotificationsSettings from './components/NotificationsSettings.vue';
import PrivacySettings from './components/PrivacySettings.vue';

const tabs: Tab[] = [
  {
    key: 'General',
    label: 'General',
    component: GeneralSettings
  },
  {
    key: 'Notifications',
    label: 'Notifications',
    component: NotificationsSettings
  },
  {
    key: 'Privacy',
    label: 'Privacy',
    component: PrivacySettings
  }
];

const currentTab = ref<TabKey>('General');
const currentTabComponent = computed(
  () => tabs.find(tab => tab.key === currentTab.value)?.component
);
</script>

<style scoped>
.fade-enter-active,
.fade-leave-active {
  transition: all 0.2s ease;
}

.fade-enter-from,
.fade-leave-to {
  opacity: 0;
}
</style>
